		</ul>
		<h4 id="432-основная-нижняя-область">4.3.2 Основная нижняя область</h4>
		<p>Здесь располагается выпадающий список выбора карты — OpenStreetMap или Яндекс.Карты — и координаты центра
			карты.</p>
		<h2 id="5-принципы-хранения-и-порядка">5. Принципы хранения и порядка</h2>
		<h3 id="разделение-данных-и-порядка">Разделение данных и порядка</h3>
		<p>Элементы хранятся в виде объектов с доступом по ID.<br>
			Порядок отображения определяется не порядком хранения, а отдельным полем <code>srt</code>.</p>
		<p>Это обеспечивает:</p>
		<ul>
			<li>предсказуемость,</li>
			<li>устойчивость при синхронизации,</li>
			<li>корректную работу после перезагрузки,</li>
			<li>независимость от особенностей JavaScript-объектов.</li>
		</ul>
		<h3 id="явное-сохранение-изменений">Явное сохранение изменений</h3>
		<p>Система использует модель «отложенного сохранения».</p>
		<p>Изменения помечаются как изменённые (dirty), но не отправляются на сервер автоматически.<br>
			Пользователь подтверждает сохранение вручную по <strong>одной-единственной кнопке «Сохранить»</strong>
			в правом
			верхнем углу окна. И только так!</p>
		<p>Это даёт:</p>
		<ul>
			<li>контроль над транзакцией,</li>
			<li>возможность пакетных изменений,</li>
			<li>предсказуемое поведение интерфейса.</li>
		</ul>
		<h2 id="6-базовый-сценарий-работы">6. Базовый сценарий работы</h2>
		<h3 id="создание-места">Создание места</h3>
		<ol>
			<li>Создать Место. Если нажать «Добавить» у папки — Место создаётся в конце списка этой папки. Если нажать
				«Добавить» у конкретного Места — новое создаётся сразу после него. Точка создаётся автоматически в
				текущем
				центре карты.</li>
			<li>Ввести название, описание, другую информацию в правой колонке.</li>
			<li>Добавить фотографии.</li>
			<li>Нажать «Сохранить» для сохранения изменений.</li>
		</ol>
		<h3 id="создание-маршрута">Создание маршрута</h3>
		<ol>
			<li>Создать Маршрут кликом по кнопке-иконке с плюсом в заголовке дерева Маршрутов в левой колонке.</li>
			<li>Ввести название, описание, другую информацию в правой колонке, добавить фотографии.</li>
			<li>Добавить Точки (кнопкой в списке точек Маршрута в правой колонке или кликом ПКМ на пустом месте
				на карте).</li>
			<li>При необходимости поправить местоположение Точек перетаскиванием маркеров на карте.</li>
			<li>При необходимости изменить порядок Точек перетаскиванием кнопок Точек в их списке в правой колонке.</li>
			<li>Сохранить изменения.</li>
		</ol>
		<p>Точка может быть добавлена повторно — система не создаёт её дубликат.</p>
		<h3 id="работа-с-изображениями">Работа с изображениями</h3>
		<ul>
			<li>Загрузить новые изображения по кнопке «Добавить фотографии».</li>
			<li>Поддерживается Drag &amp; Drop для изменения порядка отображения превьюшек.</li>
			<li>Порядок отображения обновляется также сохраняется в базе данных.</li>
		</ul>
